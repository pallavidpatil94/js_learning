// IIFE -- Immediately invoked function expression
// Self invoked Function Expression

// function expression does not require a function name and can be anonymous. 
// Function expressions can be stored in variables, passed as arguments to other functions,
//  or returned from other functions.

// Anonymous Function >>
// It is a function that does not have any name associated with it. 
// An anonymous function is not accessible after its initial creation, we use only the function keyword without the function name.
// it can only be accessed by a variable it is stored in as a function as a value. 
// An anonymous function can also have multiple arguments, but only one expression.

( function(){
    console.log("IIFE");
} )();