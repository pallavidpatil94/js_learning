// Using For-Loop
console.log(`(a). Print number from 5 to 15 incrementing by 1`);
for (let i = 5; i <= 15; i++) {
    console.log(`${i}`);
    
}

console.log(`(b). Print number from 50 to 40 decrementing by 1`);
for (let i = 50; i >= 40; i--) {
    console.log(`${i}`);
    
}

console.log(`(c). Print first 15 odd numbers`);
for (let i = 0; i <= 30; i++) {
    if(i % 2 != 0){
        console.log(i)
    }
}